    private const val CACHE_MAX_SIZE = 1000
    private val simplifyCache = ConcurrentHashMap<String, String>()
    
    private var cacheHits = 0L
    private var cacheMisses = 0L

    fun toSimplified(text: String): String {
        if (text.isEmpty()) return text
        
        simplifyCache[text]?.let {
            cacheHits++
            return it
        }
        
        val result = buildString(text.length) {
            for (char in text) {
                append(traditionalToSimplifiedMap[char] ?: char)
            }
        }
        
        if (simplifyCache.size < CACHE_MAX_SIZE) {
            simplifyCache[text] = result
        }
        
        cacheMisses++
        return result
    }
    
    fun clearCache() {
        simplifyCache.clear()
        cacheHits = 0
        cacheMisses = 0
    }
    
    fun getCacheStats(): Pair<Long, Long> = Pair(cacheHits, cacheMisses)
